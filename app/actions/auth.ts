export async function getUserIdByEmail(email: string) {
  const supabase = await createClient();
  const { data, error } = await supabase.rpc("get_user_id_by_email", {
    p_email: email,
  });

  if (error) {
    throw new Error("getUserIdByEmail error", error);
  }

  return data;
}
